#Peça uma temperatura em graus Celsius e converta para
#  Fahrenheit. A fórmula de conversão é: F = C * 9/5 + 32

print('----🌡️  Conversor de Temperatura ----')

celsius = float(input('Digite a temperatura em Celsius: '))
fahrenheit = (celsius * 9/5)+ 32
print('A temperatura em Fahrenheit é:', fahrenheit,'°F')

print('-------------------------------------')
